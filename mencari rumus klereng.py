Budi = int(input('Jumlah klereng Budi: '))
Aldyy = Budi + 15
Anto = 2 * (Aldyy + Budi)
Agung = (Aldyy + Budi + Anto) - 5
total_klereng = Budi + Anto + Agung

print('Jumlah klereng Aldy : ', Aldyy)
print('Jumlah klereng Anto : ', Anto)
print('Jumlah klereng Agung : ', Agung)
print('Jumlah klereng Agung Anto Budi: ', total_klereng)
